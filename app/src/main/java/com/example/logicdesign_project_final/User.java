package com.example.logicdesign_project_final;

public class User {
    public String Fullname, Dob, StudentID, Email, Phonenumber, ID;

    public User(){

    }

    public User(String Fullname, String Dob, String StudentID, String Email, String Phonenumber, String ID){
        this.Fullname = Fullname;
        this.Dob = Dob;
        this.StudentID = StudentID;
        this.Email = Email;
        this.Phonenumber = Phonenumber;
        this.ID = ID;
    }
}
